package com.example.turingmachine.TupelClasses;

public class TupelWrite3Code {
    public String state;
    public String write1;
    public String write2;
    public String write3;
    public String move1;
    public String move2;
    public String move3;

    public TupelWrite3Code(String state, String write1, String write2, String write3, String move1, String move2, String move3){
        this.write1=write1;
        this.write2=write2;
        this.write3=write3;
        this.state=state;
        this.move1=move1;
        this.move2=move2;
        this.move3=move3;
    }
}
