package com.example.turingmachine.TupelClasses;

public class TupelWrite2Code {
    public String state;
    public String write1;
    public String write2;
    public String move1;
    public String move2;

    public TupelWrite2Code(String state, String write1, String write2, String move1, String move2){
        this.write1=write1;
        this.write2=write2;
        this.state=state;
        this.move1=move1;
        this.move2=move2;
    }
}
